
public class OutputEx03 {

	public static void main(String[] args) {
		
		System.out.printf("%d\n", 23);
		System.out.printf("%x\n", 23);	//16진수
		System.out.printf("%o\n", 23);	//8진수
		
		System.out.printf("%f\n", 123.4567);
		System.out.printf("%.2f\n", 123.4567);
		System.out.printf("%f\n", 123.4567);
		
		System.out.printf("%e\n", 123.4567);
		
		System.out.printf("%c\n", 'x');
		
		System.out.printf("%s\n", "Hello");
		
		
	}

}
