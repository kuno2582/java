/*
true	'참'
false	'거짓'

숫자의 관점으로 이해하지 말것(중요)
소문자 true와 false를 사용해야합니다.

*/
public class VariableBoolean {
	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
//		boolean b3 = 0; 이렇게 사용하면 안됨
//		boolean b4 = TRUE; 이렇게 사용하면 안됨
		
		System.out.println(b1);
		System.out.println(b2);
		
		System.out.println(7<10);

	}
}
