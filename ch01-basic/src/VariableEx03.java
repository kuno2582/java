
public class VariableEx03 {
	public static void main(String[] args){
		
		int num1 = 0xA0E; 	// 16진수
		int num2 = 0752;	// 8진수
		int num3 = 752;		// 10진수
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		
		double e1 = 1.7e+3;
		double e2 = 1.7e-3;
		
		System.out.println(e1);
		System.out.println(e2);
		
	}
}
